def format_corrections_block(corrections: list[dict]) -> str:
    """Render top-K retrieved training corrections as a numbered block ready
    to be injected into SYSTEM_PROMPT's `{corrections}` placeholder.

    Each correction in the list is a dict with at minimum:
        guidance: human-readable instruction
        suggested_response: optional concrete sample of a better reply
        trigger_context: the original conversation snippet that produced it

    Empty list → returns "(нет применимых правил)" so the prompt still
    formats cleanly and the model knows there's nothing to weigh in.
    """
    if not corrections:
        return "(нет применимых правил)"
    lines: list[str] = []
    for i, c in enumerate(corrections, start=1):
        block = f"## Правило {i}\n**Что было не так:** {c.get('trigger_context', '—')[:300]}\n**Как надо:** {c.get('guidance', '—')}"
        sample = c.get("suggested_response")
        if sample:
            block += f"\n**Пример хорошего ответа:** {sample}"
        lines.append(block)
    return "\n\n".join(lines)


def build_chat_prompt(
    context: str,
    history: str,
    question: str,
    use_few_shots: bool = True,
    is_first_turn: bool = False,
    is_comment_mode: bool = False,
    corrections: Optional[list[dict]] = None,
    calendar_url: Optional[str] = None,
    channel: str = "website",
    call_slots: Optional[list[str]] = None,
    just_booked: Optional[str] = None,
    call_medium: Optional[str] = None,
    booked_info: Optional[str] = None,
) -> str:
    """
    Собирает финальный prompt для LLM.

    channel: канал диалога. "website" → веди в Telegram + бери email.
             Любой мессенджер (telegram/whatsapp/instagram/messenger) → вы УЖЕ в
             мессенджере: НЕ звать в telegram, НЕ просить email «для дубля»,
             команда ответит прямо здесь.

    Args:
        context: чанки из RAG retrieval (отформатированные)
        history: история диалога (последние 6 сообщений)
        question: текущий вопрос лида
        use_few_shots: вставлять ли few-shot примеры
        is_first_turn: True если это ПЕРВАЯ реплика бота в этом диалоге.
                      Срабатывает AI Act disclosure в SYSTEM_PROMPT.
        is_comment_mode: True если это публичный комментарий IG/FB
                      (не DM). Прокидывает COMMENT_MODE marker, см. секцию
                      "COMMENT MODE" в SYSTEM_PROMPT.
        corrections: список dict'ов с retrieved training corrections (top-K
                      из services/training.retrieve_corrections). Each dict
                      has trigger_context / guidance / suggested_response.
                      None или [] → блок "(нет применимых правил)".
        calendar_url: URL календаря для бронирования Discovery-call (Calendly /
                      Cal.com / etc.). Если задан — бот в handoff включает
                      ссылку на бронь (см. секцию КАЛЕНДАРЬ в SYSTEM_PROMPT).
                      None или "" → fallback на handoff без ссылки.
    """
    # Annotate the current question with the first-turn marker so the model
    # сразу видит, нужно ли вставлять AI-disclosure.
    first_marker = "[FIRST_TURN: yes]" if is_first_turn else "[FIRST_TURN: no]"
    comment_marker = "[COMMENT_MODE: yes]" if is_comment_mode else "[COMMENT_MODE: no]"
    _is_messenger = (channel or "website").lower() != "website"
    channel_marker = f"[CHANNEL: {'messenger' if _is_messenger else 'website'}]"
    # Созвон: сервер считает свободные слоты и (если есть) кладёт их сюда — бот
    # предлагает РОВНО эти времена. just_booked — лид только что подтвердил слот.
    # Наши каналы для созвона. Лиду их СПИСКОМ не зачитываем — спрашиваем открыто;
    # список нужен боту, чтобы понять «наш / не наш» канал и мягко перенаправить.
    call_block = ""
    if booked_info:
        # Созвон УЖЕ назначен ранее — не зацикливаться и не переспрашивать.
        call_block = (
            f"\n[CALL_ALREADY_BOOKED: {booked_info}] Созвон УЖЕ назначен. НЕ переспрашивай время "
            f"и канал, НЕ уточняй заново, НЕ зацикливайся — просто ответь по сути на сообщение лида; "
            f"при необходимости кратко подтверди детали ({booked_info}). Если лид ЯВНО просит "
            f"перенести — скажи, что передашь команде / подберём другое время."
        )
    elif just_booked:
        _via = f", канал — {call_medium}" if call_medium else ""
        _ask = "" if call_medium else (
            " Канал ещё не назван — спроси КОРОТКО и открыто, где удобно созвониться (НЕ зачитывай "
            "список; наши варианты — Zoom, Telegram, WhatsApp, Google Meet)."
        )
        call_block = (
            f"\n[CALL_BOOKED: {just_booked}{_via}] Лид подтвердил созвон. Кратко и тепло подтверди: "
            f"«{just_booked}{_via} — записал, напомню заранее». НЕ переспрашивай ВРЕМЯ заново.{_ask}"
        )
    elif call_slots:
        _slots = " | ".join(call_slots)
        call_block = (
            f"\n[CALL_SLOTS: {_slots}] СНАЧАЛА ответь на вопрос и собери суть задачи. ТОЛЬКО когда "
            f"задача ясна и лид заинтересован — мягко предложи созвон с сотрудником (детали, ТЗ, "
            f"оценка) и дай на выбор РОВНО эти 2 времени. Про канал спроси ОТКРЫТО («где удобно "
            f"созвониться?») — НЕ перечисляй механически все варианты. Мы созваниваемся в Zoom, "
            f"Telegram, WhatsApp или Google Meet: лид назвал это — ок; назвал другое (скайп, телефон, "
            f"вайбер) — мягко предложи выбрать из наших. Предлагай созвон ОДИН раз; если лид ещё "
            f"спрашивает — помогай и отвечай, не дави. Слоты не выдумывай — бери эти."
        )
    annotated_question = f"{question}\n{first_marker}\n{comment_marker}\n{channel_marker}{call_block}"

    corrections_text = format_corrections_block(corrections or [])

    # Handoff-блок собираем в КОДЕ — модель видит РОВНО один правильный текст.
    _cal = (calendar_url or "").strip()
    if _is_messenger:
        # Лид УЖЕ в мессенджере — не зовём «в telegram» и не просим email для дубля,
        # команда ответит прямо здесь.
        handoff_block = (
            "- RU: `Передал команде! Ребята напишут вам прямо здесь, в этом чате — оставайтесь на связи. 📩`\n"
            "- EN: `Passed to the team! They'll reply right here in this chat — stay tuned. 📩`"
        )
    elif _cal.startswith("http"):
        handoff_block = (
            f"- RU: `Передал команде! Можно сразу выбрать удобное время для короткого "
            f"созвона: {_cal} — на email тоже продублируем. 📩`\n"
            f"- EN: `Passed to the team! Grab a slot for a quick call here: {_cal} — "
            f"we'll email you too. 📩`"
        )
    else:
        handoff_block = (
            "- RU: `Передал команде! Удобнее всего продолжить в Telegram — напишите нам "
            "@deadline_corp, там быстро всё разберём и не потеряемся. На email тоже "
            "продублируем. 📩`\n"
            "- EN: `Passed to the team! Easiest to continue on Telegram — ping us "
            "@deadline_corp, faster there and nothing gets lost. We'll email you too. 📩`"
        )

    base = SYSTEM_PROMPT.format(
        context=context,
        history=history,
        question=annotated_question,
        corrections=corrections_text,
        handoff_block=handoff_block,
    )
    if use_few_shots:
        # Вставляем примеры ПЕРЕД итоговым вопросом — так модель видит их как «образец».
        # FEW_SHOT тоже содержит {calendar_url} — формат-резолвим его на calendar_url
        # (или пустую строку, чтобы пример превратился в no-calendar-вариант).
        few_shots_resolved = FEW_SHOT_EXAMPLES.replace(
            "{calendar_url}", calendar_url or "",
        )
        return base.replace(
            "# ТЕКУЩИЙ ВОПРОС ЛИДА",
            few_shots_resolved + "\n# ТЕКУЩИЙ ВОПРОС ЛИДА"
        )
    return base


